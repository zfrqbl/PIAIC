# calculate the number of seconds in a year,

# Define the main() function
def main() -> None:
    # Calculate the number of seconds in a year
    seconds_in_year: int = 365 * 24 * 60 * 60
    # Print the result
    print(f"There are {seconds_in_year} seconds in a year")


# Call the main() function

if __name__ == '__main__':
    main()

