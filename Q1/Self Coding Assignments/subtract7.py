# Create the subtract_seven function()

# This function subtracts 7 from num and returns the result

# Define the subtract_seven() function
def subtract_seven(num):
    return num - 7

# Write the main() function
def main():
    num = int(input("Enter a number: "))
    print(subtract_seven(num))

# Call the main() function
if __name__ == "__main__":
    main()  